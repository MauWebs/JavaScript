/* IGUALDAD */


// = operador de asignacion

// == operador de comparacion

// === operacion estricta (mismo tipo de dato)


//----< diferencia entre (== y ===) >----///


// (==) //
let numero = "20";

if (numero == 20) {
    console.log("Los numeros coinciden");
} else {
    console.log("Los numeros [NO] coinciden");
};


// (===) //
let numero_2 = "20";

if (numero_2 === 20) {
    console.log("Los numeros coinciden");
} else {
    console.log("Los numeros [NO] coinciden");
};
